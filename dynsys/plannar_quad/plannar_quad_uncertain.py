    def define_system_symbolic(self):
        # Symbolic states
        px, pz, phi, vx, vz, phi_dot = sp.symbols('px pz phi vx vz phi_dot')
        x = sp.Matrix([px, pz, phi, vx, vz, phi_dot])
        
        l = self.params['l'] # (m) half-width of quadrotor
        m = self.params['m'] # (kg) mass of the quadrotor
        grav = self.params['g'] # (m/s^2) gravity
        J = self.params['J'] # (kgm^2), moment of inertia

        f = sp.Matrix([
            x[3] * sp.cos(x[2]) - x[4] * sp.sin(x[2]),   # px_dot
            x[3] * sp.sin(x[2]) + x[4] * sp.cos(x[2]),   # pz_dot
            x[5],                                        # phi_dot
            x[5] * x[4] - grav * sp.sin(x[2]),           # vx_dot
            -x[5] * x[3] - grav * sp.cos(x[2]),          # vz_dot
            sp.Integer(0)                                # phi_ddot
        ])

        g = sp.Matrix([
            [0,              0],
            [0,              0],
            [0,              0],
            [0,              0],
            [1/m,            1/m],
            [l/J,           -l/J]
        ])

        # True uncertainty term: Y(x)a(theta)
        Y = sp.Matrix([[vx, vz, 0], [0, 0, phi]])
        a = np.copy(self.params["a_true"]) # true a(Theta)

        # x_dot = f(x) + g(x) ( u + Y(x) a(Theta) )
        f += g @ Y @ a  # Adding the true uncertainty to the system dynamics

        return x, f, g
    
    def define_Y_symbolic(self, x):
        # Define the symbolic uncertainty term Y(x)
        # TODO: this should be given by some neural network
        vx = x[3]
        vz = x[4]
        phi = x[2]
        Y = sp.Matrix([[vx, vz, 0], [0, 0, phi]])
        return Y

    def define_a_symbolic(self):
        # Symbolic states
        a0, a1, a2 = sp.symbols('a0 a1 a2')
        return sp.Matrix([a0, a1, a2])
    
    def ccm_closed_loop_dyn(self, t, x, x_d_fcn, u_d_fcn, dist_config):
        # x is state vector length plant.n
        x_d = x_d_fcn(t)
        u_d = u_d_fcn(t)

        uc, slack = self.ctrl_ccm(x, x_d, u_d)
        
        if dist_config["include_dist"]:
            wt = dist_config["gen_dist"](t)
            dxdt = self.dynamics(x, uc) + wt
        else:
            dxdt = self.dynamics(x, uc)

        return dxdt.ravel()

